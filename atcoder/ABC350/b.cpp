template<class T, class... Ts>
void err(T arg, Ts ... args) {
	cout << fixed << setprecision(10) << arg << ' ';
	err(args...);
}
void solve(){
	int n;cin>>n;
	vector<int>res(n+1,1);
	int ans=n;
	int q;cin>>q;
	for(int i=1;i<=q;i++){
		int x;cin>>x;
		if(res[x]){
			ans--;
			res[x]=0;
		}else{
			ans++;
			res[x]=1;
		}
	}
	cout<<ans<<'\n';
	return ;
}
signed main(){
	ios::sync_with_stdio(false);
	cin.tie(0);
	int t=1;//cin>>t;
	while(t--)solve();
	return 0;
}