template<class T, class... Ts>
void err(T arg, Ts ... args) {
    cout << fixed << setprecision(10) << arg << ' ';
    err(args...);
}
void solve(){
    int n; cin >> n;
    vector<int> a(n + 1);
    for(int i = 1; i <= n; i++) cin >> a[i];
    int cnt1 = 0, cnt2 = 0;
	for(int i = 1; i <= n; i++){
		if(a[i] % 2 == 1)cnt1++;
		else cnt2++;
	}
	int ans = 0;
	if(cnt2 > 0){
		ans = 1 + cnt1;
	}else{
		ans = cnt1 - 1;
	}
	cout << ans << '\n';
    return ;
}
signed main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    int t = 1;
    cin >> t;
    while(t--)solve();
    return 0;
}