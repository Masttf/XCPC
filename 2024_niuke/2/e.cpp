template<class T, class... Ts>
void err(T arg, Ts ... args) {
	cout << fixed << setprecision(10) << arg << ' ';
	err(args...);
}
void solve(){
	int n; cin >> n;
	
	int res = n;
	for(int i = 0 ;i <= 63; i++){
		if(n >> i & 1){
			res = n - (1ll << i);
			if(res > 0){
				cout << res << '\n';
				return ;
			}
		}
	}
	cout << - 1 << '\n';
	return ;
}
